// Funktion zum Laden eines Bitmap-Arrays (1 Byte pro Pixelreihe) von SD
inline bool loadBitmapFromSD(const char* filename, uint8_t* buffer, size_t expectedSize) {
    File bmpFile = SD.open(filename);
    if (!bmpFile) return false;
    if (static_cast<size_t>(bmpFile.available()) < expectedSize) {
        bmpFile.close();
        return false;
    }
    bmpFile.read(buffer, expectedSize);
    bmpFile.close();
    return true;
}

